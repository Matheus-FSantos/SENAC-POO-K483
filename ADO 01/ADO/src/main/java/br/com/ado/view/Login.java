    private void poluiConta(Conta contas[]) {
        String nome = "";
        String senha = "";
        Cliente cliente = null;
        Conta conta = null;
        
        for(int i = 0; i < contas.length; i++){
            nome = "usuario" +(i + 1);
            senha = "senha" + (i + 1);
            
            cliente = new Cliente(nome, senha);
            conta = new Conta(cliente);
            
            contas[i] = conta;
        }
    }

    private boolean verificaCliente(Cliente cliente) {
        for(Conta conta : contas){
            if(conta.getCliente().getNome().equals(cliente.getNome()) && conta.getCliente().getSenha().equals(cliente.getSenha())){
                if(conta.getCliente().getHierarquia().equals("VIP")){
                    return true;
                }
            }
        }
        
        return false;
    }
