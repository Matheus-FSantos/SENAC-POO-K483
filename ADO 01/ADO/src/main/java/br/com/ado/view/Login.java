        String senha = this.senhaInput.getText();
        
        Cliente cliente = new Cliente(nome, senha);
        
        Integer testes = 0;
        
        boolean resultado = verificaCliente(cliente);
        
        if(resultado){
            JOptionPane.showMessageDialog(this, "Logado com sucesso!", "Sucesso!", JOptionPane.INFORMATION_MESSAGE);
            System.exit(0);
        } else {
            JOptionPane.showMessageDialog(this, "Voce não tem permissão!", "Erro!", JOptionPane.ERROR_MESSAGE);
            this.nomeInput.setText("");
            this.senhaInput.setText("");
            
            testes++;
            
            if(testes == 3){
                System.exit(0);
            }
        }
